    @Override
    public void update(Map<InetAddress, PeerNode> peers) {
        Iterator it = peers.values().iterator();
        while (it.hasNext())   {
            JTextField pTxt = new JTextField(it.next().toString());
            peerPne.add(pTxt);
        }
        repaint();
    }
    
    
